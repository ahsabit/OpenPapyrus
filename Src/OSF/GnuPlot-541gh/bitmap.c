//
void b_vector(GpTermEntry_Static * pThis, uint x, uint y)
{
	GnuPlot * p_gp = pThis->P_Gp;
	// We can't clip properly, but we can refuse to draw out of bounds 
	if(x < pThis->MaxX && y < pThis->MaxY && p_gp->_Bmp.b_currx < pThis->MaxX && p_gp->_Bmp.b_curry < pThis->MaxY)
		p_gp->_Bmp.WLine(p_gp->_Bmp.b_currx, p_gp->_Bmp.b_curry, x, y);
	p_gp->_Bmp.b_currx = x;
	p_gp->_Bmp.b_curry = y;
}
//
// put text str at (x,y) with color b_value and rotation b_angle
//
void b_put_text(GpTermEntry_Static * pThis, uint x, uint y, const char * str)
{
	GnuPlot * p_gp = pThis->P_Gp;
	if(p_gp->_Bmp.b_angle == 1)
		x += p_gp->_Bmp.b_vchar / 2;
	else
		y -= p_gp->_Bmp.b_vchar / 2;
	switch(p_gp->_Bmp.b_angle) {
		case 0:
		    for(; *str; ++str, x += p_gp->_Bmp.b_hchar)
			    p_gp->_Bmp.Putc(x, y, *str, p_gp->_Bmp.b_angle);
		    break;
		case 1:
		    for(; *str; ++str, y += p_gp->_Bmp.b_hchar)
			    p_gp->_Bmp.Putc(x, y, *str, p_gp->_Bmp.b_angle);
		    break;
	}
}

